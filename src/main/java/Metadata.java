public class Metadata {
    private int numRacks;
    private int numPods;
    private int maxTimestamp;
    private int numPackets;

    public Metadata() {
    }

    public int getNumRacks() {
        return numRacks;
    }

    public int getNumPods() {
        return numPods;
    }

    public int getNumPackets() {
        return numPackets;
    }

    public int getMaxTimestamp() {
        return maxTimestamp;
    }
}
